class Customer {
  final String id;
  final String name;
  final String dob;
  final String address;
  final String email;
  final String phone;
  final String pass;

  Customer({
    required this.id,
    required this.name,
    required this.dob,
    required this.address,
    required this.email,
    required this.phone,
    required this.pass,
  });
}