class Employee {
  final String id;
  final String name;
  final String address;
  final String email;
  final String phone;
  final String pass;
  final String role;

  Employee({
    required this.id,
    required this.name,
    required this.address,
    required this.email,
    required this.phone,
    required this.pass,
    required this.role,
  });
}